def exp_needed(level: int) -> int:
    level = max(1, int(level or 1))
    return EXP_BASE + (level - 1) * EXP_STEP


def level_title(level: int) -> str:
    level = max(1, int(level or 1))
    return LEVEL_TITLES[level - 1] if level <= 200 else LEVEL_TITLES[-1]


def rank_title(level: int) -> str:
    level = max(1, int(level or 1))
    tier = (level - 1) // 100 + 1
    if tier <= len(RANK_NAMES):
        return RANK_NAMES[tier - 1]
    return f"🌈 {tier}等彩虹"


def progress_info(level: int, level_exp: int, total_exp: int) -> Dict[str, int | str]:
    level = max(1, int(level or 1))
    level_exp = max(0, int(level_exp or 0))
    need = exp_needed(level)
    # 若舊資料超出需求，顯示仍以目前級需求為上限；真正升級由 add_exp 處理。
    current = min(level_exp, need)
    percent = min(100, int(current * 100 / need)) if need else 100
    filled = min(10, max(0, percent // 10))
    return {
        "level": level,
        "current": current,
        "needed": need,
        "remaining": max(0, need - current),
        "percent": percent,
        "bar": "█" * filled + "░" * (10 - filled),
        "total_exp": max(0, int(total_exp or 0)),
    }


def normalize_owned_title(value: str | None) -> str:
    return str(value or "").strip()
